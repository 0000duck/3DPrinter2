using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using _3DPrinter.utils;

namespace _3DPrinter.setting.model
{
    public class CommonSettingsModel : NotifyPropertyChangedBase
    {


        private int _maxLayer = 0;
        public int MaxLayer
        {
            get { return _maxLayer; }
            set
            {
                _maxLayer = value;
                OnPropertyChanged("MaxLayer");
            }
        }


        private int _minLayer = 0;
        public int MinLayer
        {
            get { return _minLayer; }
            set
            {
                _minLayer = value;
                OnPropertyChanged("MinLayer");
            }
        }

        private int _lowerLayer = 0;
        public int LowerLayer
        {
            get { return _lowerLayer; }
            set
            {
                _lowerLayer = value;
                OnPropertyChanged("LowerLayer");
            }
        }

        private int _higherLayer = 0;
        public int HigherLayer
        {
            get { return _higherLayer; }
            set
            {
                _higherLayer = value;
                OnPropertyChanged("HigherLayer");
            }
        }


    }
}
